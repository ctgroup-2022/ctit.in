    <p align="justify">Grievance Redressal System is a vital part of any administration. It is the responsibility of the College Administration to provide a secure and contented environment to all its Staff and Students. The Grievance Redressal Committee has been formed in the CTIT, Shahpur College as per the UGC guidelines to redress the grievances of both the staff and the students. Since the inception of the college, the Committee has been under the direct purview of the Principal. The grievances received by the Principal are forwarded to the concerned Committee members who look in to the problems depending upon the seriousness of the issue. The Committee has been continuously striving to take the best efforts possible to create a harmonious and conducive atmosphere to everyone in question.</p>
