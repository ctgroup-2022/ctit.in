    <!-- Approach start -->
    <h4>Functions of the Grievance Redressal Committee</h4>
    <ul align="justify">
        <li>To accept written grievances from students and staff related to the system.</li>
        <li>To create and implement a mechanism to handle the reported grievances.</li>
        <li>To forward the findings to the Management if necessary for further action.</li>
        <li>To listen, record and scrutinize the grievances submitted to them by the Staff and Students and take necessary steps immediately.</li>
        <li>To attend to the grievances based on the authenticity and gravity of the criticisms made.</li>
        <li>To represent the grievances to the concerned section which may include maintenance, transport, academic, amenities etc.</li>
        <li>To convene periodical meetings to discuss whether the grievances have been settled.</li>
        <li>To make a follow-up of these matters at regular intervals till their final disposal.</li>
        <li>To maintain strict confidentiality, if necessary.</li>
    </ul>
