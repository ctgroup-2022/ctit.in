        <p align="justify">Bachelor of Technology (B.Tech.) in Civil Engineering is an undergraduate degree program that focuses on the study of designing, constructing, and maintaining infrastructure projects such as buildings, bridges, roads, dams, and other structures. It is a four-year program that equips students with a comprehensive understanding of civil engineering principles and practices.</p>
        <p align="justify">Throughout the program, students engage in both theoretical coursework and practical hands-on experiences. They learn to use industry-standard software tools for structural analysis, design simulations, and project management. They also participate in laboratory experiments, field surveys, and site visits to gain practical insights into real-world engineering challenges.</p>
        <u><h3>Eligibility Criteria</h3></u>
        <p align="justify">All those candidates who have passed 10+2 examination from a Board recognized or established by Central/State Government through a legislation and a member of Council of Boards of School Education (COBSE), New Delhi with Physics / Mathematics / Chemistry / Computer Science / Electronics / Information Technology / Biology / Informatics Practices / Biotechnology / Technical Vocational subject / Agriculture / Engineering Graphics / Business Studies.</p>
        <h5 class="text-center">OR</h5>
        <p align="justify">Those candidates who have passed minimum 03 years Diploma examination from Punjab State Board of Technical Education & Industrial Training, Chandigarh or Sant Longowal Institute of Engineering and Technology, Longowal (SLIET), or any such examination from any other recognized State Board of Technical Education with atleast 45% marks (40% in case of candidates belonging to reserved category) subject to vacancies in the First Year, in case the vacancies at lateral entry are exhausted.</p>
