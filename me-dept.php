        <h3 class="text-center">About Department</h3><hr>
            <p align="justify"> 
            Mechanical Engineering has the reputation of being an ever green, incessant and perpetually growing branch and its relevance is essentially undeniable. It deals with design and production of tools, machines and all other mechanical equipment used in any industry. Mechanical engineers use the principles of mathematics, material science, physics and economics to design, manufacture and maintain all the mechanical equipments. The motive of the Department of Mechanical engineering is concerned with all types of machinery in industries and all aspects of their mechanism and functioning, the design, development, construction, production, installation, operation and maintenance; such as large steam and gas turbines, components of thermal power stations, internal combustion engines, jet engines, machine tools, air conditioning and heating machines, refrigerators etc to name a few. They not only design and create new products, but also develop materials for them and ways of making them.
            </p>
            <p align="justify">
            The department has highly qualified, competent, dedicated and distinguished faculty members to impart quality education, project guidance and personality development through enhancement of communication skills and group discussions. Seminars are conducted to keep abreast of the latest development in the industry. With frequent industrial visits, our students are given fair opportunities to make up their mind for future career options. 
            </p>
          
